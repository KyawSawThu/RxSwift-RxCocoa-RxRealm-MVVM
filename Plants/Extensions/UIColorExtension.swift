import UIKit

extension UIColor {
    static var primary: UIColor {
        return UIColor(named: SharedConstants.Colors.PRIMARY_COLOR)!
    }
    static var accent: UIColor {
        return UIColor(named: SharedConstants.Colors.ACCENT_COLOR)!
    }
}

